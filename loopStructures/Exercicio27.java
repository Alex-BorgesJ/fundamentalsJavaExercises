public class Exercicio27 {
	
	public static void main (String[] args) {
		
		int k = 10;
		
		for (int i = 1; i <= 10; i++){
			
			System.out.print(i + ", ");
			
			for (int j = 1; j <= k; j++){
				
				System.out.print(j + " ");
				
				}
			
			System.out.println();
			k = k - 1;
			}
		
	}
}

