public class TestaTiposPrimitivos{
	
	public static void main (String[] args) {
		
		boolean tipoBool = true;
        byte tipoByte = 127;
        short tipoShort = 32767;
        int tipoInteiro = 2147483647;
        long tipoLong = 9223372036854775807L;
        float tipoFloat = 3.14f;
        double tipoDouble = 3.141592653589793;
        char tipoCaractere = 'A';
		
		System.out.println(tipoBool);
		System.out.println(tipoByte);
		System.out.println(tipoShort);
		System.out.println(tipoInteiro);
		System.out.println(tipoLong);
		System.out.println(tipoFloat);
		System.out.println(tipoDouble);
		System.out.println(tipoCaractere);
		
	}
}

